	public static int getBaseID(int blockID) {
		int first, last, middle;

		if (blockID >= block[block.length - 1].id) return 899;

		first = 0;
		last = 897;

		while (true) {
			middle = (first + last) / 2;
			if (block[middle].id <= blockID && block[middle + 1].id > blockID) return middle;
			if (block[middle].id > blockID) last = middle;
			else first = middle;
		}
	}
